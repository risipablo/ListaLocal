// Seccion de carrito

class Carrito {
    constructor(){
        const carritoLocal = JSON.parse(localStorage.getItem("carrito"))

        this.carrito = carritoLocal || [];
        this.total = 0;
        this.totalProductos = 0;
        this.listar();
    }

    estaenelCarrito({ id }) {
        return this.carrito.find((producto) => producto.id === id);
    }

    agregar(producto){
        const productoencarrito = this.estaenelCarrito(producto);
        if (productoencarrito) {
            productoencarrito.cantidad ++;
        } else {
            this.carrito.push({...producto,cantidad:1});
        }

        localStorage.setItem("carrito", JSON.stringify(this.carrito));

        this.listar();

        Toastify({
            text: `${producto.nombre} ${producto.tamaño} de ${producto.kg}kg fue agregado`,
            position: "center",
            className: "info",
            gravity: "bottom",
            style: {
            background: "linear-gradient(to right, violet, red)",
            },
        }).showToast();
    }

    quitar(id){
        const indice = this.carrito.findIndex((producto) => producto.id === id);
        if (this.carrito[indice].cantidad > 1 ) {
            this.carrito[indice].cantidad--;
        } else {
            this.carrito.splice(indice, 1);
        }
        localStorage.setItem("carrito" , JSON.stringify(this.carrito));
        this.listar();
    }

    vaciar() {
        this.carrito = [];
        localStorage.removeItem("carrito");
        this.listar();
    }

    resetearcarrito(){
        
    }

    listar (){
        this.total = 0;
        this.subtotal = 0;
        this.totalProductos = 0;
        divCarrito.innerHTML= "";

        for(const producto of this.carrito){
            divCarrito.innerHTML += `
            <div class="productoCarrito">

            <h4 class="producto"> ${producto.nombre} </h4>
            <p class="descripcion"> ${producto.tamaño}</p>
            <p class="descripcion"> ${producto.kg}</p>
            <p class="precio"> Precio: $${producto.precio}</p>
            <div class="items">
            <p class="cantidad">Cantidad: ${producto.cantidad}</p>
            <a href="#" data-id="${producto.id}" class="btnQuitar"><i class="fa-solid fa-trash-can">  </i>  </a>
            <p class="precio">Subtotal: $${this.subtotal = producto.cantidad * producto.precio}</p>
            </div>
            </div>
            `;

            
            this.total += producto.precio * producto.cantidad;
            this.totalProductos += producto.cantidad;
        }

        if (this.totalProductos > 0) {
            botonComprar.classList.remove("oculto"); 
        } else {
            botonComprar.classList.add("oculto")
        }
        
        const botonesQuitar = document.querySelectorAll(".btnQuitar");
        for (const boton of botonesQuitar) {
            boton.onclick = (event) => {
            event.preventDefault();
            this.quitar(Number(boton.dataset.id));
            };
        }

        
        spanCantidadProductos.innerText = this.totalProductos;
        spanTotalCarrito.innerText = this.total;
        
    }
}




